package Arrays;

import java.util.Arrays;

public class Concatenation {
    public static void main(String[] args) {
        int[] arr={1,2,3};
        int[] answer= concatenate(arr);
        System.out.println(Arrays.toString(answer));
    }

    static int[] concatenate(int[] nums){

        int n=nums.length;
        int[] ans= new int[2*n];

        for (int i=0;i<n;i++){

            ans[i]=nums[i];
            ans[i+n]=nums[i];
        }
        return ans;
    }

}
